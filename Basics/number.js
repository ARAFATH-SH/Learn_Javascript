let x = 5.475;

console.log(x.toExponential(2));  // Exponential notation with 2 decimal places
console.log(x.toExponential(4));  // Exponential notation with 4 decimal places
console.log(x.toFixed(2));  // Fixed-point notation with 2 decimal places
console.log(x.toFixed(4));  // Fixed-point notation with 4 decimal places
console.log(x.toPrecision(3));  // Precision of 3 significant digits
console.log(x.toPrecision(5));  // Precision of 5 significant digit s
